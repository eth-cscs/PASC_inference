function [u, v] = pm(a,b)

% Plus Minus Function
% 
% USAGE:
%    [u v] = pm(a,b)
% OUPUT:
%    u = (a + b)/sqrt(2);
%    v = (a - b)/sqrt(2);
%
% WAVELET SOFTWARE AT POLYTECHNIC UNIVERSITY, BROOKLYN, NY
% http://taco.poly.edu/WaveletSoftware/

u = (a + b)/sqrt(2);
v = (a - b)/sqrt(2);
